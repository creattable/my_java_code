
public class hiworld{
	public static void main(String[] args){
		//自我介绍
		System.out.print("a1");//print输出后不换行
		System.out.println("a2");//println输出后换行
		
		System.out.println("a3\n");//"\n"代换换行
		System.out.println("a4");
		
		
		System.out.println("111111111111");
		System.out.println("1111\t22222222");
		System.out.println("11111\t2222222");
		System.out.println("111111\t222222");
		
		
	}
	/*
	\字符称为转义字符，将后面的字母转换成其他含义
	
	\t比较特殊 空出来一个制表符的距离（一个制表符8位）
	空出来的数量是动态的、跟当前有没有一个制表符有关
	*/
	
	
}